let activeProject = null;
let activeIndex = 0;

function setMenu(open) {
  menuToggle.setAttribute("aria-expanded", String(open));
  menuToggle.setAttribute("aria-label", open ? "Закрыть меню" : "Открыть меню");
  navigation.classList.toggle("is-open", open);
  body.classList.toggle("is-menu-open", open);
}

menuToggle.addEventListener("click", () => {
  setMenu(menuToggle.getAttribute("aria-expanded") !== "true");
});

navigation.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => setMenu(false));
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 960) setMenu(false);
});

async function copyText(text) {
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return;
  }

  const textArea = document.createElement("textarea");
  textArea.value = text;
  textArea.setAttribute("readonly", "");
  textArea.style.position = "fixed";
  textArea.style.opacity = "0";
  document.body.append(textArea);
  textArea.focus({ preventScroll: true });
  textArea.select();
  textArea.setSelectionRange(0, text.length);

  let copied = false;
  try {
    copied = document.execCommand("copy");
  } finally {
    textArea.remove();
  }
  if (!copied) throw new Error("Copy command failed");
}

if (copyEmailButton) {
  const label = copyEmailButton.querySelector(".copy-email-label");
  let resetTimer;

  copyEmailButton.addEventListener("click", async () => {
    clearTimeout(resetTimer);

    try {
      await copyText(copyEmailButton.dataset.email);
      label.textContent = "E-mail скопирован";
      copyEmailButton.classList.add("is-copied");
    } catch {
      label.textContent = "Не удалось — выделите адрес";
      copyEmailButton.classList.remove("is-copied");
    }

    resetTimer = window.setTimeout(() => {
      label.textContent = "Скопировать e-mail";
      copyEmailButton.classList.remove("is-copied");
    }, 2400);
  });
}

function normalizedImage(project, index) {
  const image = project.images[index];
  if (Array.isArray(image)) {
    return {
      src: image[0],
      alt: image[1],
      title: project.title,
      meta: project.meta,
      description: project.description,
      points: project.points,
    };
  }
  return { ...image, points: image.points || project.points };
}

function renderPoints(points) {
  galleryPoints.innerHTML = "";
  galleryPoints.hidden = points.length === 0;
  points.forEach(([heading, text]) => {
    const item = document.createElement("li");
    const strong = document.createElement("strong");
    strong.textContent = heading;
    item.append(strong, document.createTextNode(text));
    galleryPoints.append(item);
  });
}

function renderGallery() {
  const project = projects[activeProject];
  const image = normalizedImage(project, activeIndex);

  galleryNumber.textContent = project.number;
  galleryKicker.textContent = project.kicker;
  galleryImage.src = image.src;
  galleryImage.alt = image.alt;
  galleryTitle.textContent = image.title;
  galleryMeta.textContent = image.meta;
  galleryDescription.textContent = image.description;
  galleryCurrent.textContent = String(activeIndex + 1);
  galleryTotal.textContent = String(project.images.length);
  renderPoints(image.points || []);

  galleryThumbnails.querySelectorAll("button").forEach((button, index) => {
    const isActive = index === activeIndex;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-current", isActive ? "true" : "false");
  });
}

function buildThumbnails(project) {
  galleryThumbnails.innerHTML = "";
  project.images.forEach((_, index) => {
    const image = normalizedImage(project, index);
    const button = document.createElement("button");
    const thumbnail = document.createElement("img");

    button.type = "button";
    button.setAttribute("aria-label", `Открыть изображение ${index + 1}`);
    thumbnail.src = image.src;
    thumbnail.alt = "";
    thumbnail.loading = "lazy";
    button.append(thumbnail);
    button.addEventListener("click", () => {
      activeIndex = index;
      renderGallery();
    });
    galleryThumbnails.append(button);
  });
}

function openGallery(projectKey) {
  activeProject = projectKey;
  activeIndex = 0;
  buildThumbnails(projects[projectKey]);
  renderGallery();
  gallery.showModal();
  body.classList.add("is-gallery-open");
}

function closeGallery() {
  gallery.close();
  body.classList.remove("is-gallery-open");
}

function changeSlide(delta) {
  const count = projects[activeProject].images.length;
  activeIndex = (activeIndex + delta + count) % count;
  renderGallery();
}

document.querySelectorAll("[data-project]").forEach((button) => {
  const projectKey = button.dataset.project;
  button.setAttribute("aria-label", `Открыть галерею: ${projects[projectKey].title}`);
  button.addEventListener("click", () => openGallery(projectKey));
});

galleryPrev.addEventListener("click", () => changeSlide(-1));
galleryNext.addEventListener("click", () => changeSlide(1));
galleryClose.addEventListener("click", closeGallery);

gallery.addEventListener("click", (event) => {
  if (event.target === gallery) closeGallery();
});

gallery.addEventListener("close", () => {
  body.classList.remove("is-gallery-open");
});

document.addEventListener("keydown", (event) => {
  if (!gallery.open) return;
  if (event.key === "ArrowLeft") changeSlide(-1);
  if (event.key === "ArrowRight") changeSlide(1);
});

if ("IntersectionObserver" in window) {
  document.documentElement.classList.add("reveal-enabled");
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      });
    },
    { rootMargin: "0px 0px -7%", threshold: 0.08 },
  );

  document.querySelectorAll("[data-reveal]").forEach((element) => revealObserver.observe(element));
}
